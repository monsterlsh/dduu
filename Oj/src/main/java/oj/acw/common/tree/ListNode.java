package oj.acw.common.tree;

/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) {
 *         val = x;
 *         next = null;
 *     }
 * }
 */
public class ListNode {
    int val;
    public ListNode next;
    public ListNode(int x) {
        val = x;
        next = null;
     }
  }
